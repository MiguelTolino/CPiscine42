#include <stdlib.h>

int		ft_strlen(char *str)
{
	int i;

	i = 0;
	while (*(str + i) != '\0')
	{
		i++;
	}
	return (i);
}

char	*ft_strcpy(char *dest, char *src)
{
	int i;

	i = 0;
	while (*(src + i))
	{
		*(dest + i) = *(src + i);
		i++;
	}
	*(dest + i) = '\0';
	return (dest);
}

char	*ft_strdup(char *src)
{
	int count;
	int *dest;

	count = ft_strlen(src);
	dest = malloc(sizeof(char) * (count + 1));
	dest = ft_strcpy(dest, src);
	if (dest == NULL)
	{
		return (NULL);
	}
	return (dest);
}
